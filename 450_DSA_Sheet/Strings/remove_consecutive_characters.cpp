class Solution{
    public:
    string removeConsecutiveCharacter(string S)
    {
        int size = S.size();
        string ans(size,'x');
        for(int i=0;i<size;i++){
            if(S[i] != S[i+1])
                ans[i] = S[i];
        }
        return ans;
    }
};

