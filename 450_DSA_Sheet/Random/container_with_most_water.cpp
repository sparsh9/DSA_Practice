class Solution {
public:
    int maxArea(vector<int>& v) {

        int left = 0;
        int n = v.size();
        int right = n - 1;
        int area = 0;

        while(left < right){
            area = max(area, min(v[left],v[right]) * abs(left - right));
            if(v[left] < v[right]) left++;
            else right--;
        }

        return area;
        
    }
};


